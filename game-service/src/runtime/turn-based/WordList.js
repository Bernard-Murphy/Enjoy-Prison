  _ensureMixed: function () {
    var all = [];
    for (var cat in this.lists) {
      if (cat !== "mixed") {
        var list = this.lists[cat];
        for (var i = 0; i < list.length; i++) {
          all.push(list[i]);
        }
      }
    }
    this.lists.mixed = all;
  },

  init: function () {
    this._ensureMixed();
  },

  getRandomWord: function (category, minLen, maxLen) {
    if (!this.lists.mixed.length) this._ensureMixed();
    var pool = this.lists[category] || this.lists.mixed;
    var filtered = [];
    for (var i = 0; i < pool.length; i++) {
      var w = pool[i];
      if (w.length >= minLen && w.length <= maxLen) {
        filtered.push(w);
      }
    }
    if (filtered.length === 0) filtered = pool;
    return filtered[Math.floor(Math.random() * filtered.length)].toUpperCase();
  },

  getWordleWord: function (length) {
    if (!this.lists.mixed.length) this._ensureMixed();
    var filtered = [];
    for (var i = 0; i < this.lists.mixed.length; i++) {
      var w = this.lists.mixed[i];
      if (w.length === length) filtered.push(w);
    }
    if (filtered.length === 0) {
      for (var j = 0; j < this.lists.common.length; j++) {
        var c = this.lists.common[j];
        if (c.length === length) filtered.push(c);
      }
    }
    if (filtered.length === 0) return "HELLO";
    return filtered[Math.floor(Math.random() * filtered.length)].toUpperCase();
  },

  isValidWord: function (word) {
    if (!this.lists.mixed.length) this._ensureMixed();
    var lower = word.toLowerCase();
    for (var i = 0; i < this.lists.mixed.length; i++) {
      if (this.lists.mixed[i] === lower) return true;
    }
    return false;
  },
